// FOLKS, ATTENTION HERE:
// Some parts of the magic you are going to see are replicated from Helm templating engine.
// We decided to use Golang templating and add, more or less, the same extra functionality to this operator
// for people who are already comfortable with Helm. Not all the extra functionality was added to keep this simpler.
// Ref: https://github.com/helm/helm/blob/main/pkg/engine/funcs.go

func EvaluateTemplate(templateString string, data map[string]interface{}) (result string, err error) {
	templateFunctionsMap := GetFunctionsMap()

	// Create a Template object from the given string
	parsedTemplate, err := template.New("main").Funcs(templateFunctionsMap).Parse(templateString)
	if err != nil {
		return result, err
	}

	// Create a new buffer to store the templating result
	buffer := new(bytes.Buffer)

	err = parsedTemplate.Execute(buffer, data)
	if err != nil {
		return result, err
	}

	return buffer.String(), nil
}

// GetFunctionsMap return a map with equivalency between functions for inside templating and real Golang ones
func GetFunctionsMap() template.FuncMap {
	f := sprig.TxtFuncMap()

	// Delete risky functions
	// Ref: http://masterminds.github.io/sprig/os.html
	delete(f, "env")
	delete(f, "expandenv")

	// Add some extra functionality
	extra := template.FuncMap{
		"toToml":        toTOML,
		"toYaml":        toYAML,
		"fromYaml":      fromYAML,
		"fromYamlArray": fromYAMLArray,
		"toJson":        toJSON,
		"fromJson":      fromJSON,
		"fromJsonArray": fromJSONArray,

		// Extended funcs
		"logPrintf": logPrintf,
	}

	for k, v := range extra {
		f[k] = v
	}

	return f
}

// logPrintf is the equivalent of printf function.
// It takes a format-string and several items as arguments and throw the result by logs.
// It returns an empty string as returning something is required in Go template's func mapping
func logPrintf(format string, v ...interface{}) string {
	log.Printf(format, v...)
	return ""
}

// toYAML takes an interface, marshals it to yaml, and returns a string. It will
// always return a string, even on marshal error (empty string).
//
// This is designed to be called from a template.
func toYAML(v interface{}) string {
	data, err := yaml.Marshal(v)
	if err != nil {
		// Swallow errors inside of a template.
		return ""
	}
	return strings.TrimSuffix(string(data), "\n")
}

// fromYAML converts a YAML document into a map[string]interface{}.
//
// This is not a general-purpose YAML parser, and will not parse all valid
// YAML documents. Additionally, because its intended use is within templates
// it tolerates errors. It will insert the returned error message string into
// m["Error"] in the returned map.
func fromYAML(str string) map[string]interface{} {
	m := map[string]interface{}{}

	if err := yaml.Unmarshal([]byte(str), &m); err != nil {
		m["Error"] = err.Error()
	}
	return m
}

// fromYAMLArray converts a YAML array into a []interface{}.
//
// This is not a general-purpose YAML parser, and will not parse all valid
// YAML documents. Additionally, because its intended use is within templates
// it tolerates errors. It will insert the returned error message string as
// the first and only item in the returned array.
func fromYAMLArray(str string) []interface{} {
	a := []interface{}{}

	if err := yaml.Unmarshal([]byte(str), &a); err != nil {
		a = []interface{}{err.Error()}
	}
	return a
}

// toTOML takes an interface, marshals it to toml, and returns a string. It will
// always return a string, even on marshal error (empty string).
//
// This is designed to be called from a template.
func toTOML(v interface{}) string {
	b := bytes.NewBuffer(nil)
	e := toml.NewEncoder(b)
	err := e.Encode(v)
	if err != nil {
		return err.Error()
	}
	return b.String()
}

// toJSON takes an interface, marshals it to json, and returns a string. It will
// always return a string, even on marshal error (empty string).
//
// This is designed to be called from a template.
func toJSON(v interface{}) string {
	data, err := json.Marshal(v)
	if err != nil {
		// Swallow errors inside of a template.
		return ""
	}
	return string(data)
}

